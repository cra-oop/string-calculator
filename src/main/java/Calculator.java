public class Calculator {

    public int add(int l, int r) {
        return l + r;
    }
    public int sub(int l, int r) {
        return l - r;
    }
    public int mul(int l, int r) {
        return l * r;
    }
    public int div(int l, int r) throws ArithmeticException {
        return l / r;
    }

}
